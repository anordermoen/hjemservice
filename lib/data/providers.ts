export function getProviderById(id: string): ServiceProvider | undefined {
  return providers.find((p) => p.userId === id);
}

export function getProvidersByCategory(categoryId: string): ServiceProvider[] {
  return providers.filter((p) => p.categories.includes(categoryId));
}

export function getReviewsByProviderId(providerId: string): Review[] {
  return reviews.filter((r) => r.providerId === providerId);
}

export function getFeaturedProviders(): ServiceProvider[] {
  return providers.filter((p) => p.rating >= 4.8).slice(0, 4);
}
